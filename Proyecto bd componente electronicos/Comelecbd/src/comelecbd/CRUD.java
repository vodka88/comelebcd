         {com_TC_tipo.addItem(list_TC_tipo.get(i).toString());}
        
        for (i=0;i<tamlistsubtipo;i++)
         {com_TC_subtipo.addItem(list_TC_subtipo.get(i).toString());}
        
        for (i=0;i<tamlistclase;i++)
         {com_TC_clase.addItem(list_TC_clase.get(i).toString());}
